var a = 10;
document.writeln("Result: <br>");
document.writeln("The value of (a) is: " + a + "<br>");
document.writeln(".................................................<br><br>");
++a;
document.writeln("The value of (++a) is: " + a + "<br>");
document.writeln("Now the value of (a) is: " + a + "<br><br>");
--a;
a++;
document.writeln("The value of (a++) is: " + a + "<br>");
